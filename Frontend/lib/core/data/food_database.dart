  /// İsme göre besin ara (Türkçe karakter duyarsız)
  static List<Food> search(String query) {
    if (query.trim().isEmpty) return foods;
    final lower = query.toLowerCase().trim();
    final normalized = _normalizeTurkish(lower);
    return foods.where((f) {
      final nameNorm = _normalizeTurkish(f.name.toLowerCase());
      return nameNorm.contains(normalized);
    }).toList();
  }

  static String _normalizeTurkish(String s) {
    return s
        .replaceAll('ı', 'i')
        .replaceAll('ğ', 'g')
        .replaceAll('ü', 'u')
        .replaceAll('ş', 's')
        .replaceAll('ö', 'o')
        .replaceAll('ç', 'c')
        .replaceAll('İ', 'i')
        .replaceAll('Ğ', 'g')
        .replaceAll('Ü', 'u')
        .replaceAll('Ş', 's')
        .replaceAll('Ö', 'o')
        .replaceAll('Ç', 'c');
  }

  /// ID ile besin bul
  static Food? findById(String id) {
    try {
      return foods.firstWhere((f) => f.id == id);
    } catch (_) {
      return null;
    }
  }

  /// Proteini yüksek besinler (100g'da >15g protein)
  static List<Food> getHighProteinFoods() {
    return foods.where((f) => f.proteinPer100g > 15).take(8).toList();
  }

  /// Düşük kalorili atıştırmalıklar (<100 kcal/100g, porsiyon <150g)
  static List<Food> getLowCalorieSnacks() {
    return foods.where((f) =>
        f.caloriesPer100g < 100 && f.portionSizeGrams <= 150).take(8).toList();
  }
